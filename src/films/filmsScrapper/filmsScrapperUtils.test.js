const { smallImgToBigImg } = require('./filmsScrapperUtils')

test("transforms url of small poster to url of big poster", () => {
  const smallPoster =
    "https://m.media-amazon.com/images/M/MV5BMDFkYTc0MGEtZmNhMC00ZDIzLWFmNTEtODM1ZmRlYWMwMWFmXkEyXkFqcGdeQXVyMTMxODk2OTU@._V1_UX182_CR0,0,182,268_AL_.jpg";

  const expected =
    "https://m.media-amazon.com/images/M/MV5BMDFkYTc0MGEtZmNhMC00ZDIzLWFmNTEtODM1ZmRlYWMwMWFmXkEyXkFqcGdeQXVyMTMxODk2OTU@._V1_.jpg";
  
  const actual = smallImgToBigImg(smallPoster);
  expect(actual).toBe(expected);
});